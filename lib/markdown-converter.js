/**
 * lib/markdown-converter.js
 * Converts scraped conversation turns into two output formats:
 *
 *  1. Context Passport — compressed clipboard text
 *     A compact summary of the conversation for re-priming a new chat.
 *     Long assistant responses are truncated; user messages are kept full.
 *
 *  2. Full Markdown Export — complete .md file
 *     All turns, full content, proper headings, metadata block.
 *
 * Exported as global: MarkdownConverter
 */

'use strict';

const MarkdownConverter = (() => {

  // ── Constants ───────────────────────────────────────────────────────────────

  const PASSPORT_ASSISTANT_MAX = 500;  // chars per assistant turn in passport
  const PASSPORT_USER_MAX      = 800;  // chars per user turn in passport

  // ── Helpers ─────────────────────────────────────────────────────────────────

  /**
   * Format a Date object as a human-readable string.
   * @param {Date} [date]
   * @returns {string}
   */
  function formatDate(date = new Date()) {
    return date.toLocaleString('en-GB', {
      year:   'numeric',
      month:  'long',
      day:    'numeric',
      hour:   '2-digit',
      minute: '2-digit',
      timeZoneName: 'short',
    });
  }

  /**
   * Truncate text to maxLen chars, breaking at a word boundary.
   * Appends '…' if truncated.
   * Preserves any opening code fence if the text starts with one.
   * @param {string} text
   * @param {number} maxLen
   * @returns {string}
   */
  function truncate(text, maxLen) {
    if (!text || text.length <= maxLen) return text;

    // Try to break at whitespace
    const cut = text.slice(0, maxLen);
    const lastSpace = cut.lastIndexOf(' ');
    const truncated = lastSpace > maxLen * 0.8 ? cut.slice(0, lastSpace) : cut;
    return truncated.trim() + '…';
  }

  /**
   * Escape special characters in content that might break
   * the markdown structure (only | in table cells handled separately).
   */
  function safeContent(text) {
    return text || '*(empty)*';
  }

  // ── Format 1: Context Passport ───────────────────────────────────────────────

  /**
   * Generate a compact context passport string suitable for clipboard.
   *
   * @param {Array<{role: string, content: string}>} turns
   * @param {object} meta
   *   @param {string} [meta.conversationId]
   *   @param {string} [meta.model]
   * @returns {string}
   */
  function toPassport(turns, meta = {}) {
    const {
      conversationId = 'unknown',
      model          = 'unknown',
    } = meta;

    const lines = [];

    // ── Header ──────────────────────────────────────────────────────────────
    lines.push('# Context Passport');
    lines.push('');
    lines.push(`Conversation: ${conversationId}`);
    lines.push(`Model:        ${model}`);
    lines.push(`Exported:     ${formatDate()}`);
    lines.push(`Turns:        ${turns.length}`);
    lines.push('');
    lines.push('---');
    lines.push('');

    if (turns.length === 0) {
      lines.push('*(No conversation turns found)*');
      return lines.join('\n');
    }

    // ── Turns ────────────────────────────────────────────────────────────────
    for (const turn of turns) {
      const roleLabel = turn.role === 'user' ? '[User]' : '[Assistant]';
      const maxLen    = turn.role === 'user' ? PASSPORT_USER_MAX : PASSPORT_ASSISTANT_MAX;
      const content   = truncate(safeContent(turn.content), maxLen);

      lines.push(`${roleLabel}`);
      lines.push(content);
      lines.push('');
    }

    // ── Footer ───────────────────────────────────────────────────────────────
    lines.push('---');
    lines.push('*Generated by Claude Ultimate Extension*');

    return lines.join('\n');
  }

  // ── Format 2: Full Markdown Export ──────────────────────────────────────────

  /**
   * Generate a full, untruncated markdown document.
   *
   * @param {Array<{role: string, content: string}>} turns
   * @param {object} meta
   *   @param {string} [meta.conversationId]
   *   @param {string} [meta.model]
   * @returns {string}
   */
  function toMarkdown(turns, meta = {}) {
    const {
      conversationId = 'unknown',
      model          = 'unknown',
    } = meta;

    const now = new Date();
    const lines = [];

    // ── Document header ──────────────────────────────────────────────────────
    lines.push('# Claude Conversation Export');
    lines.push('');
    lines.push('| Field | Value |');
    lines.push('|-------|-------|');
    lines.push(`| Model | \`${model}\` |`);
    lines.push(`| Conversation | \`${conversationId}\` |`);
    lines.push(`| Date | ${formatDate(now)} |`);
    lines.push(`| Turns | ${turns.length} |`);
    lines.push('');
    lines.push('---');
    lines.push('');

    if (turns.length === 0) {
      lines.push('*(No conversation turns found)*');
      lines.push('');
      lines.push('> **Note:** The conversation may still be loading, or Claude Ultimate');
      lines.push('> was unable to locate message elements in the current DOM.');
      return lines.join('\n');
    }

    // ── Turns ────────────────────────────────────────────────────────────────
    for (let i = 0; i < turns.length; i++) {
      const turn = turns[i];
      const turnNum = i + 1;
      const roleDisplay = turn.role === 'user' ? 'User' : 'Claude';
      const roleEmoji   = turn.role === 'user' ? '👤' : '🤖';

      lines.push(`## Turn ${turnNum} — ${roleEmoji} ${roleDisplay}`);
      lines.push('');
      lines.push(safeContent(turn.content));
      lines.push('');

      // Add separator between turns (not after the last one)
      if (i < turns.length - 1) {
        lines.push('---');
        lines.push('');
      }
    }

    // ── Footer ───────────────────────────────────────────────────────────────
    lines.push('');
    lines.push('---');
    lines.push('');
    lines.push(`*Exported with [Claude Ultimate Extension](https://claude.ai) on ${formatDate(now)}*`);

    return lines.join('\n');
  }

  // ── File download helper ─────────────────────────────────────────────────────

  /**
   * Trigger a browser file download with the given text content.
   * Uses a temporary <a> element — no new tab opens.
   *
   * @param {string} content   - File text
   * @param {string} filename  - Suggested filename
   * @param {string} [mimeType]
   */
  function downloadAsFile(content, filename, mimeType = 'text/markdown;charset=utf-8') {
    const blob = new Blob([content], { type: mimeType });
    const url  = URL.createObjectURL(blob);

    const a = document.createElement('a');
    a.href     = url;
    a.download = filename;
    a.style.display = 'none';
    document.body.appendChild(a);
    a.click();

    // Cleanup — revoke after a tick
    setTimeout(() => {
      document.body.removeChild(a);
      URL.revokeObjectURL(url);
    }, 200);
  }

  /**
   * Generate a safe filename from the conversation ID and current date.
   * @param {string} conversationId
   * @returns {string}
   */
  function makeFilename(conversationId) {
    const date = new Date()
      .toISOString()
      .slice(0, 16)           // "2025-04-19T14:30"
      .replace('T', '_')
      .replace(/:/g, '-');    // "2025-04-19_14-30"
    const shortId = conversationId.slice(0, 8);
    return `claude-conversation-${shortId}-${date}.md`;
  }

  // ── Public API ───────────────────────────────────────────────────────────────

  return {
    toPassport,
    toMarkdown,
    downloadAsFile,
    makeFilename,
    formatDate,
    truncate,       // exported for testing
  };

})();
